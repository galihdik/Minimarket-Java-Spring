public class Roles {
	private Long idRoles;
	private String nameRoles;
	private String codeRoles;

	public Long getIdRoles() {
		return idRoles;
	}

	public void setIdRoles(Long idRoles) {
		this.idRoles = idRoles;
	}

	public String getNameRoles() {
		return nameRoles;
	}

	public void setNameRoles(String nameRoles) {
		this.nameRoles = nameRoles;
	}

	public String getCodeRoles() {
		return codeRoles;
	}

	public void setCodeRoles(String codeRoles) {
		this.codeRoles = codeRoles;
	}
}
